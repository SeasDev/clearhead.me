        </div>
        <div class="gs--full">
          <p>Further, the measurable success of the last redesign may be a function of vague expectations and qualitative goals rather than clearly defined goals and financial measurement. Only 34% said the reason for a redesign was to improve sales or profitability. And the number one actual reason for doing their last redesign was the site was “stale”.</p>
          <p>Based on the research, interviews and our experience, we hypothesize the following factors will likely raise the bar for expectations of future redesigns:</p>
          <ul class="purple-bars">
            <li>Flattening of organic ecommerce growth</li>
            <li>Increased competition (especially from Amazon)</li>
            <li>Maturing of the mobile channel experience</li>
            <li>Greater availability of precise performance data</li>
            <li>Higher customer expectations due to the ubiquity of sophisticated UX</li>
          </ul>
          <p>For the above reasons, we believe the redesigns of the future will feel different. What will or should the next wave look like?</p>
        </div>
        <div class="questions-wrap">
          <div class="gs--full future-item">
            <div class="question">
              <p>1. More iterative redesign approach.</p>
            </div>
            <div class="answer">
              <p>A big-bang redesign creates risk and ambiguity. The risk is in making many decisions that go into one launch,  lowering the opportunity to validate each with customer feedback and data before launch. Second, performance ambiguity is created because it’s difficult to deconstruct what worked and what didn’t after an all-in-one launch. </p>
              <p>In contrast, we may see a broadening of the definition of “redesigns” in the future, evolving to include an increasingly popular iterative approach to validating priorities and UX decisions, testing, launching, and repeating that process for different parts of the site. This modern approach will allow each phase to have more confidence coming in and more data coming out on the impact. </p>
