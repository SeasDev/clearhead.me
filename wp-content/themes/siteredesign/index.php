  <section class="grid-row grid-row-padding--full grid-row-width--wide intro">
    <p>In a world where digital products can be iteratively changed and continuously optimized, it’s still commonplace for organizations to undertake disruptive full site or product redesign projects in shorter, specific time boxes. These projects are generally incredibly resource intensive and carry a high degree of risk and reward. </p>
    <p>While so much about digital consumer and business behavior has changed in the last 20 years, it seems that the redesign, as a project and practice, has survived significantly intact for many businesses. With investments this sizable, we wanted to gain clarity on the ecommerce website redesign process including the motivations for their undertaking, the extent to which data is injected into the process, how success is defined and measured and the problem patterns that frequently emerge. </p>
    <p>Clearhead, an award-winning digital optimization company, frequently partners with leading ecommerce brands to create better customer experiences through testing and personalization driven by data. With that in mind, we decided to go straight to the source. We connected with over 200 ecommerce leaders who have been through multiple redesigns in their careers to better understand how redesigns are contemplated and executed today. </p>
    <p>We conducted both a survey and in-depth interviews, setting out to answer some key questions: </p>
    <ol class="key-quest">
      <li><p class="ol">1</p> How are redesigns typically defined?</li>
      <li><p class="ol">2</p> What are the best reasons to initiate a site redesign? </li>
      <li><p class="ol">3</p> What is actually driving redesigns?</li>
      <li><p class="ol">4</p> How is data used in preparation for a redesign?</li>
      <li><p class="ol">5</p> What capabilities are most critical and lacking in executing a redesign?</li>
      <li><p class="ol">6</p> What was the confidence going into a redesign?</li>
      <li><p class="ol">7</p> What were the measures of success?</li>
      <li><p class="ol">8</p> What were the results of the redesign?</li>
      <li><p class="ol">9</p> Where do executives versus managers differ on these questions?</li>
    </ol>
