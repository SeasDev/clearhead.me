        <div class="gs--full para-wrap">
          <p>On the other hand, 44% would consider iterative changes over a period to be a redesign. While redesigns have typically been considered a “change the business” initiative, lines are blurring with “run the business”. Interviews suggest the trend may be heading towards iterative redesigns by folks who have experienced big-bang redesigns in the past.</p>
        </div>
