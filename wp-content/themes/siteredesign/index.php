        <div class="gs--full margin-wrap">
          <p>The next era of redesigns may prove more difficult. </p>
          <p>Many respondents we studied and talked to about their last redesign reported their sites had mobile experiences and UX issues that were so bad, they were confident that their judgement or best practices would lead them to a better site. They also agreed for their next redesign, they’ll need to test assumptions more stringently to get the next level of performance. </p>
